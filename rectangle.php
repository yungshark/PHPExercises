<?php

	class Rectangle
	{
		public $height;
		public $width;

		public function _contruct($height, $width){
			$this-> = $height;
			$this-> = $width;
		}

		public function area($height, width){
			return $this->getHeight(); * $this->getWidth();
		}

		public function getHeight(){
			return $this->height;
		}
		public function getWidth(){
			return $this->width;
		}

	}